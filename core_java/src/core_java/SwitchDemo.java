package core_java;

public class SwitchDemo {
	public static void main(String[] args) {
		int size = 48;
		switch (size) {
		case 34:
			System.out.println("S");
			break;
		case 38:
			System.out.println("M");
			break;
		case 40:
			System.out.println("L");
			break;
		case 42:
			System.out.println("XL");
			break;
		default:
			System.out.println(" size Unknown");

		}

	}
}
