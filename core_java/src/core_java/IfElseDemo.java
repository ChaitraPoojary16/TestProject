package core_java;

public class IfElseDemo {
	public static void main(String[] args) {
		int x = 5;
		if (x > 0) {
			System.out.println("x is positive");
		} else {
			System.out.println("x is Negative");

		}

		int y = -15;
		if (y > 0) {
			System.out.println("y is positive");
		} else {
			System.out.println("y is Negative");

		}
	}
}
